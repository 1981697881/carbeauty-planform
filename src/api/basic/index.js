import request from '@/utils/request'
import {
  getToken
} from '@/utils/auth'

// 项目-获取列表
export function getProjectList(params, data) {
  const url = '/projectCar/projectList/' + params.pageNum + '/' + params.pageSize
  return request({
    url: url,
    headers: {
      'authorization': getToken('cinerx'),
      'Content-Type': 'application/json'
    },
    method: 'POST',
    data: data
  })
}

